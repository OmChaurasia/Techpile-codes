// WAP to find sum and average of three given numbers

#include <stdio.h>
#include <conio.h>
void main()
{
    int a, b, c;
    a = 25;
    b = 75;
    c = 50;
    printf("The sum of %d, %d and %d is :%d\n", a, b, c, a + b + c);
    printf("The average of %d, %d and %d is :%d", a, b, c, (a + b + c) / 3);
}